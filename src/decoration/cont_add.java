    public void combo_fill(){
         Connection c = null;
        Statement s =null;
        ResultSet rs= null;
        try{
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:decor.db");
            c.setAutoCommit(false);
            s=c.createStatement();
            String c_fill="select cont from cont_combo";
            rs=s.executeQuery(c_fill);
            while(rs.next()){
                String n=rs.getString("cont");
                jComboBox1.addItem(n);
            }
            s.close();
            rs.close();
            c.commit();
            c.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"برجاء اغلاق البرنامج واعاده تشغيله");
        }
        finally{
            try{
               
           rs.close();
            s.close();
            c.commit();
            c.close();
            }catch(Exception e){}
        
        }
    }
